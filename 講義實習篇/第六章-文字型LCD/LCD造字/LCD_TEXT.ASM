EXTRN  CODE(CLEAR_LCD,SET_LCD,WLCD_COMMAND,WLCD_DATA,DELAY_01MS,FLOOK_TABLE)

ORG	00H
JMP	START

        
START:	CALL		SET_LCD    
		CALL		CLEAR_LCD
		MOV	R0,#0
		MOV	DPTR,#TABLE
		//將文字寫入CGRAM
		MOV	R2,#01000000B
		CALL	   WLCD_COMMAND
LOOP:	MOV	A,R0
		MOV	R1,A
		CALL	FLOOK_TABLE
		CALL	WLCD_DATA
		INC R0
		//8行x2筆=16
		CJNE	R0,#16,LOOP
		//設定在第一行第一個位址
		MOV	R2,#080H
		CALL	   WLCD_COMMAND
		//呼叫寫入CGRAM第0個位置的資料
		MOV	R2,#000H
		CALL	   WLCD_DATA
		//設定在第二行第一個位址
		MOV	R2,#081H
		CALL	   WLCD_COMMAND
		//呼叫寫入CGRAM第1個位置的資料
		MOV	R2,#001H
		CALL	   WLCD_DATA			
		JMP	$

TABLE:	DB	00AH,00AH,011H,004H,008H,012H,01FH,000H
		DB	00EH,000H,01FH,00AH,00AH,00AH,011H,000H
	
	

END

