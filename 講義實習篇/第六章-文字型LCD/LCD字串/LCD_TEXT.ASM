EXTRN  CODE(CLEAR_LCD,SET_LCD,WLCD_COMMAND,WLCD_DATA,DELAY_01MS,FLOOK_TABLE)

ORG	00H
JMP	START

        
START:	CALL		SET_LCD    
		CALL		CLEAR_LCD

		MOV	R1,#0
		//設定在第一行第一個位址
		MOV	R2,#080H
		CALL	WLCD_COMMAND
		MOV	DPTR,#TABLE01
LOOP:	CALL	FLOOK_TABLE
		CALL	WLCD_DATA   		
		INC	R1
		CJNE	R1,#20,LOOP

		MOV	R1,#0
		//設定在第二行第一個位址
		MOV	R2,#0C0H
		CALL	WLCD_COMMAND
		MOV	DPTR,#TABLE02
LOOP1:	CALL	FLOOK_TABLE
		CALL	WLCD_DATA   		
		INC	R1
		CJNE	R1,#20,LOOP1
		JMP	$

TABLE01:	DB	"MCUEE is famous "
		
TABLE02:	DB	"department      "
	
	

END

